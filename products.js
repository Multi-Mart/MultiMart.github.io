/* Determine which product to show.
 * Priority: ?id=N in URL, then data-id on <body>, then default to "8"
 */
(function() {
    function getProductId() {
        var params = new URLSearchParams(window.location.search);
        var id = params.get('id');
        if (id && window.MULTIMART_PRODUCTS[id]) return id;
        var bodyId = document.body.getAttribute('data-id');
        if (bodyId && window.MULTIMART_PRODUCTS[bodyId]) return bodyId;
        return "8";
    }

    function formatTaka(n) {
        return "৳" + n.toLocaleString('en-BD');
    }

    function render() {
        var id = getProductId();
        var p = window.MULTIMART_PRODUCTS[id];

        // Update page title and meta
        document.title = p.name + " — MultiMart | Handcrafted Sculpture from Dhaka";
        var metaDesc = document.querySelector('meta[name="description"]');
        if (metaDesc) metaDesc.setAttribute('content', p.tagline);

        // Eyebrow
        var eyebrow = document.querySelector('.hero-eyebrow');
        if (eyebrow) eyebrow.textContent = p.eyebrow;

        // Title
        var title = document.querySelector('.hero-title');
        if (title) title.textContent = p.name;

        // Price
        var price = document.querySelector('.hero-price');
        if (price) price.textContent = formatTaka(p.price);

        // Stock
        var stock = document.querySelector('.hero-stock');
        if (stock) stock.innerHTML = '<span class="stock-dot"></span> ' + p.stock;

        // Rating
        var rating = document.querySelector('.hero-rating .rating-text');
        if (rating) rating.textContent = p.rating.toFixed(1) + ' · Hand-finished in our Dhaka workshop';

        // Tagline
        var tagline = document.querySelector('.hero-tagline');
        if (tagline) tagline.textContent = p.tagline;

        // Specs
        var specsEl = document.querySelector('.hero-quick-specs');
        if (specsEl) {
            specsEl.innerHTML = '';
            p.specs.forEach(function(s) {
                var li = document.createElement('li');
                li.innerHTML = '<strong>' + s[0] + '</strong><span>' + s[1] + '</span>';
                specsEl.appendChild(li);
            });
        }

        // Main image
        var mainImg = document.getElementById('heroImage');
        if (mainImg) mainImg.src = 'img/product-img/' + id + '.jpg';

        // Thumbnails
        var thumbsContainer = document.querySelector('.hero-thumbs');
        if (thumbsContainer) {
            thumbsContainer.innerHTML = '';
            p.thumbs.forEach(function(t, i) {
                var btn = document.createElement('button');
                btn.className = 'hero-thumb' + (i === 0 ? ' active' : '');
                btn.setAttribute('data-src', 'img/product-img/' + t);
                var labelMap = {0: 'Front view', 1: 'Side view', 2: 'Detail', 3: 'In context'};
                btn.setAttribute('data-label', labelMap[i] || 'View');
                btn.innerHTML = '<img src="img/product-img/' + t + '" alt="' + (labelMap[i] || 'View') + '">';
                btn.addEventListener('click', function() {
                    document.querySelectorAll('.hero-thumb').forEach(function(x) { x.classList.remove('active'); });
                    btn.classList.add('active');
                    mainImg.src = btn.getAttribute('data-src');
                });
                thumbsContainer.appendChild(btn);
            });
        }

        // Story sections
        var piece = document.querySelector('.story-piece');
        if (piece) piece.textContent = p.story.piece;
        var craft = document.querySelector('.story-craft');
        if (craft) craft.textContent = p.story.craft;
        var care = document.querySelector('.story-care');
        if (care) care.textContent = p.story.care;
        var custom = document.querySelector('.story-custom');
        if (custom) custom.textContent = p.story.custom;

        // WhatsApp links (with product name + price)
        var waPrimary = 'https://wa.me/8801752510807?text=Hi%20MultiMart!%20I%27d%20like%20to%20order%20the%20' +
            encodeURIComponent(p.name) + '%20(' + encodeURIComponent(formatTaka(p.price)) + ').';
        var waCustom = 'https://wa.me/8801752510807?text=Hi%20MultiMart!%20I%27d%20like%20to%20customize%20the%20' +
            encodeURIComponent(p.name) + '%20(different%20size%2Fcolor%2Ffinish).';
        var waFloat = 'https://wa.me/8801752510807?text=Hi%20MultiMart!%20I%20saw%20' +
            encodeURIComponent(p.name) + '%20and%20would%20like%20to%20order.';
        var waMobile = 'https://wa.me/8801752510807?text=Hi%20MultiMart!%20I%27d%20like%20to%20order%20' +
            encodeURIComponent(p.name) + '%20(' + encodeURIComponent(formatTaka(p.price)) + ').';

        document.querySelectorAll('a.hero-btn-primary').forEach(function(a) { a.href = waPrimary; });
        document.querySelectorAll('a.hero-btn-secondary').forEach(function(a) { a.href = waCustom; });
        var floatBtn = document.querySelector('a[href*="Classical%20Bust%20No.%208"]');
        if (floatBtn) floatBtn.href = waFloat;
        var mobileCta = document.querySelector('.sticky-mobile-cta a');
        if (mobileCta) {
            mobileCta.href = waMobile;
            mobileCta.textContent = 'Order on WhatsApp — ' + formatTaka(p.price);
        }

        // Breadcrumb current
        var bcCurrent = document.querySelector('.bc-current');
        if (bcCurrent) bcCurrent.textContent = p.name;

        // Update <body data-id> for share-link consistency
        document.body.setAttribute('data-id', id);
    }

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', render);
    } else {
        render();
    }
})();